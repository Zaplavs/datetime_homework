from datetime import datetime

def format_news_date():
    """
    Функция, которая возвращает текущую дату и день недели 
    в формате "Сегодня Понедельник, 26 сентября 2025 г.".
    
    Returns:
        str: Отформатированная строка с текущей датой
    """
    # Словарь для перевода английских названий дней недели на русский
    weekdays = {
        0: 'Понедельник',
        1: 'Вторник', 
        2: 'Среда',
        3: 'Четверг',
        4: 'Пятница',
        5: 'Суббота',
        6: 'Воскресенье'
    }
    
    # Словарь для перевода английских названий месяцев на русский
    months = {
        1: 'января', 2: 'февраля', 3: 'марта', 4: 'апреля',
        5: 'мая', 6: 'июня', 7: 'июля', 8: 'августа',
        9: 'сентября', 10: 'октября', 11: 'ноября', 12: 'декабря'
    }
    
    today = datetime.now()
    
    weekday = weekdays[today.weekday()]
    day = today.day
    month = months[today.month]
    year = today.year
    
    return f"Сегодня {weekday}, {day} {month} {year} г."
